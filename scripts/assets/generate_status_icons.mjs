// Generate status-effect icons (buff/debuff/aura) from game-icons.net (CC BY 3.0).
//
// HUD_UI_OVERHAUL §4/§13: the buff/debuff strip was the biggest information gap,
// and §13 names status icons as the single largest asset need. game-icons.net
// ships flat single-path silhouettes — exactly the "icon + shape + color, never
// color alone" treatment the status pips want. This is the reproducible
// "download": it reads the vendored @iconify-json/game-icons data package, pulls
// the curated subset mapped to our status vocabulary, and emits:
//
//   1. public/assets/ui/status/<token>.svg — one standalone currentColor SVG per
//      status token. Auditable, shipped assets (ASSETS.md row + manifest pick
//      them up). Delete them and the build still boots: the pip falls back to the
//      2-letter procedural glyph.
//   2. src/engine/status-glyphs.generated.ts — the same path data baked for the
//      runtime so the DOM pip can inline the silhouette synchronously.
//
// License: CC BY 3.0. Attribution is recorded in ASSETS.md and CREDITS.md and
// must stay shipped. Re-run after editing STATUS_ICON_MAP:
//   npm run generate:status-icons
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const HERE = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(HERE, '..', '..');
const SVG_OUT_DIR = path.join(ROOT, 'public', 'assets', 'ui', 'status');
const TS_OUT = path.join(ROOT, 'src', 'engine', 'status-glyphs.generated.ts');

// Status token → a game-icons name. Tokens are the StatusId values plus the
// derived buff sub-kinds the HUD classifies (shield/regen/power/haste/dot/aura/
// buff/debuff). Every token the pips can produce is covered, so none fall back.
const STATUS_ICON_MAP = {
  // hard crowd-control / debuffs (red family)
  stun: 'knockout',
  root: 'tree-roots',
  silence: 'silenced',
  hex: 'frog',
  slow: 'snail',
  disarm: 'sword-break',
  blind: 'blindfold',
  fear: 'terror',
  taunt: 'enrage',
  break: 'cracked-shield',
  cyclone: 'tornado',
  sleep: 'night-sleep',
  frozen: 'ice-cube',
  // protective / utility (cool family)
  invis: 'invisible',
  'magic-immune': 'magic-shield',
  // derived buff sub-kinds
  shield: 'checked-shield',
  regen: 'health-normal',
  power: 'muscle-up',
  haste: 'sprint',
  dot: 'poison-cloud',
  aura: 'aura',
  buff: 'sparkles',
  debuff: 'despair'
};

const ATTRIBUTION = {
  source: 'game-icons.net',
  authors: 'Lorc, Delapouite & contributors',
  url: 'https://game-icons.net',
  license: 'CC BY 3.0',
  licenseUrl: 'https://creativecommons.org/licenses/by/3.0/'
};

function pathData(body) {
  const m = body.match(/\sd="([^"]+)"/);
  if (!m) throw new Error(`no path data in: ${body.slice(0, 80)}`);
  return m[1];
}

function svgFile(d, size) {
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${size} ${size}" width="${size}" height="${size}"><path fill="currentColor" d="${d}"/></svg>\n`;
}

function main() {
  const data = require('@iconify-json/game-icons/icons.json');
  const size = data.width ?? 512;

  fs.rmSync(SVG_OUT_DIR, { recursive: true, force: true });
  fs.mkdirSync(SVG_OUT_DIR, { recursive: true });
  const missing = [];

  const tokenPaths = {};
  const tokenSourceIcons = {};
  for (const [token, name] of Object.entries(STATUS_ICON_MAP)) {
    const icon = data.icons[name];
    if (!icon) {
      missing.push(`status ${token} -> ${name}`);
      continue;
    }
    const d = pathData(icon.body);
    tokenPaths[token] = d;
    tokenSourceIcons[token] = name;
    fs.writeFileSync(path.join(SVG_OUT_DIR, `${token}.svg`), svgFile(d, size));
  }

  if (missing.length) {
    console.error('Missing icons in @iconify-json/game-icons:');
    for (const m of missing) console.error(`  - ${m}`);
    process.exit(1);
  }

  const block = (obj) => Object.keys(obj).sort().map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(obj[k])}`).join(',\n');
  const ts = `// GENERATED by scripts/assets/generate_status_icons.mjs — do not edit by hand.
// Status-effect icons from ${ATTRIBUTION.source} (${ATTRIBUTION.authors}), ${ATTRIBUTION.license}.
// ${ATTRIBUTION.url} · attribution: ASSETS.md and CREDITS.md.
// Run \`npm run generate:status-icons\` to regenerate.

export const STATUS_ICON_VIEWBOX = ${size};

/** status token → SVG path data. Inlined by the HUD status pips (§4). */
export const STATUS_ICON_PATHS: Record<string, string> = {
${block(tokenPaths)}
};

/** token → source game-icons name, for provenance and credits. */
export const STATUS_ICON_SOURCE_ICONS: Record<string, string> = {
${block(tokenSourceIcons)}
};

export const STATUS_ICON_ATTRIBUTION = ${JSON.stringify(ATTRIBUTION, null, 2)} as const;
`;
  fs.writeFileSync(TS_OUT, ts);

  console.log(`generated ${Object.keys(tokenPaths).length} status SVGs in ${path.relative(ROOT, SVG_OUT_DIR)}`);
  console.log(`wrote ${path.relative(ROOT, TS_OUT)}`);
}

main();
